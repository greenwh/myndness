  <section class="info-section">
    <h2 class="section-title">Stretching Guidelines</h2>
    <div class="tips-content">
      <h3 class="tips-subtitle">What to Look For in Videos</h3>
      <ul class="tips-list">
        <li>Duration: 5-15 minutes for beginners</li>
        <li>Pace: Slow holds (15-30 seconds per stretch)</li>
        <li>Instructor: Clear verbal cues and demonstrations</li>
        <li>Difficulty: Beginner or senior-friendly</li>
      </ul>

      <h3 class="tips-subtitle">Safety Guidelines</h3>
      <ul class="tips-list">
        <li>Never force a stretch - gentle tension is enough</li>
        <li>Breathe naturally and slowly throughout each stretch</li>
        <li>Hold stretches for 15-30 seconds</li>
        <li>Stop immediately if you feel sharp pain</li>
        <li>Warm up with light movement before stretching</li>
        <li>These gentle stretches are safe for users with pacemakers</li>
      </ul>

      <h3 class="tips-subtitle">Getting Started</h3>
      <ul class="tips-list">
        <li>Start with seated stretches if balance is a concern</li>
        <li>Have a chair or wall nearby for support</li>
        <li>Stretch both sides equally (left and right)</li>
        <li>Practice daily for best results</li>
        <li>Focus on areas where you feel the most tension</li>
      </ul>

      <h3 class="tips-subtitle">Common Areas to Stretch</h3>
      <ul class="tips-list">
        <li><strong>Neck:</strong> Gentle side bends and rotations</li>
        <li><strong>Shoulders:</strong> Arm circles and shoulder rolls</li>
        <li><strong>Upper back:</strong> Seated twists and chest openers</li>
        <li><strong>Lower back:</strong> Knee-to-chest and cat-cow stretches</li>
        <li><strong>Hips:</strong> Figure-four stretch (seated or lying)</li>
        <li><strong>Legs:</strong> Hamstring and calf stretches</li>
      </ul>
    </div>
